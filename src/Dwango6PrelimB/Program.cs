    class Program
    {
        static void Main(string[] args)
        {
            Solver solver = new Solver();
            solver.Solve();
        }
    }

    class Solver
    {
        public Solver()
        {
            Input input = new Input();
            N = input.Long();
            X = input.ArrayLong();
            D = new long[N - 1];
            for (int i = 0; i < N - 1; i++)
            {
                D[i] = X[i + 1] - X[i];
            }
        }

        private long N;

        private long[] X;

        // 各スライムの距離
        private long[] D;
        private long MOD = 1000000007;

        public void Solve()
        {
            var multi = getMulti(N - 1);
            long ans = 0;
            for (int i = 0; i < N - 1; i++)
            {
                ans = (ans + multi[i] * D[i] % MOD) % MOD;
            }

            Console.WriteLine(ans);
        }

        private Dictionary<long, long[]> Memo = new Dictionary<long, long[]>();

        public long[] getMulti(long n)
        {
            if (Memo.ContainsKey(n))
            {
                return Memo[n];
            }

            if (n == 1)
            {
                long[] a = new long[1];
                a[0] = 1;
                Memo.Add(1, a);
                return a;
            }

            long[] b = new long[n];
            // 一つ選ぶときに移動させる距離の合計
            for (int i = 0; i < n; i++)
            {
                b[i] = i + 1;
            }

            long[] l = getMulti(n - 1);

            // i番目のスライムを合体させたとき
            // i = 0は特別扱いする

            for (int j = 0; j < n - 1; j++)
            {
                b[j + 1] += l[j];
            }

            for (int i = 1; i < n; i++)
            {
                // 1つ少ない数のスライムの組み合わせ方について、計算する
                for (int j = 0; j < n; j++)
                {
                    if (j >= i)
                    {
                        b[j] += l[j - 1];
                    }

                    if (j < i)
                    {
                        b[j] += l[j];
                    }
                }
            }

            Memo.Add(n, b);
            return b;
        }
    }
